#include<stdio.h>

/**
 * main - entry point
 *
 * description: print numbers from zero to 9
 *
 * return: 0 (success)
*/

int main(void)
{
        int digit = '0';

        while(digit <= 9)
	{
		printf("%i", digit);
		digit++;
	}
	print('\n');

	return (0);
}
